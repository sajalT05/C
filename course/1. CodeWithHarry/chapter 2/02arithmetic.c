#include<stdio.h>

int main() {
printf("arithmetic operations\n");
int a=15;
int b=6;
// addition
printf("a+b: %d\n", a+b);
// substraction
printf("a-b: %d\n", a-b);
// multiplication
printf("a*b: %d\n", a*b);
// division
printf("a/b: %d\n", a/b);
// remainder
printf("a .. b: %f\n", a%b);

return 0;
}